#include <iostream>
#include <cmath>

using namespace std;

int main(void)
{
    cout << "Int size: " << sizeof(int) << endl;
    cout << "Float size: " << sizeof(float) << endl;
    cout << "Double size: " << sizeof(double) << endl;
    cout << "2^8 - 1: " << pow(2,8) << endl;
    cout << 15*16+8 << endl;
    return 0;
}